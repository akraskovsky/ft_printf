#include <stdlib.h>
#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = (char *)malloc(size + 1);
	if (ptr != NULL)
		while (i <= size)
			ptr[i++] = '\0';
	return (ptr);
}
