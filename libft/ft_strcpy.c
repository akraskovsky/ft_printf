#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*d;
	char	*s;

	d = dst;
	s = (char *)src;
	while (*s != '\0')
		*d++ = *s++;
	*d = '\0';
	return (dst);
}
